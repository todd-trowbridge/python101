# print strings with quotes
print("hello world")
print('hello world')

# print multiline strings with triple quotes
print('''hello world''')
print("""hello world""")
print('''
Hello
My name is Todd
''')

# escape character '\'
print('I\'m a human')

# concatenate
print('statement 1 ' + 'statement 2')

# convert other 'types' to string first with str()
print('this is a int: ' + str(1+1))